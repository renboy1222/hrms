    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (this.title.equals("Add")) {
            int response = JOptionPane.showConfirmDialog(jFrameHRMS, "Are you sure to save " + jTextFieldType.getText() + " ?", "Save confirmation", JOptionPane.YES_NO_OPTION);
            if (response == JOptionPane.YES_OPTION) {
                this.roomType.setId(null);
                this.roomType.setType(jTextFieldType.getText());
                this.roomType.setCapacity(Integer.parseInt(jTextFieldCapacity.getText()));
                this.roomType.setDescription(jTextFieldDescription.getText());
                roomTypeDAOImpl.addRoomType(roomType);
                this.dispose();
            }
        } else if (this.title.equals("Update")) {
            int response = JOptionPane.showConfirmDialog(jFrameHRMS, "Are you sure to update " + jTextFieldType.getText() + " ?", "Save confirmation", JOptionPane.YES_NO_OPTION);
            if (response == JOptionPane.YES_OPTION) {
                this.roomType.setType(jTextFieldType.getText());
                this.roomType.setCapacity(Integer.parseInt(jTextFieldCapacity.getText()));
                this.roomType.setDescription(jTextFieldDescription.getText());
                roomTypeDAOImpl.updateRoomType(roomType);
                this.dispose();
            }
        } else if (this.title.equals("Delete")) {
            int response = JOptionPane.showConfirmDialog(jFrameHRMS, "Are you sure to delete " + jTextFieldType.getText() + " ?", "Save confirmation", JOptionPane.YES_NO_OPTION);
            if (response == JOptionPane.YES_OPTION) {
                roomTypeDAOImpl.deleteRoomType(roomType);
                this.dispose();
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed


