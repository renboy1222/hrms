    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (this.title.equals("Add")) {
            int response = JOptionPane.showConfirmDialog(jFrameHRMS, "Are you sure to save " + jTextFieldName.getText() + " ?", "Save confirmation", JOptionPane.YES_NO_OPTION);
            if (response == JOptionPane.YES_OPTION) {
                this.customer.setId(null);
                this.customer.setName(jTextFieldName.getText());
                this.customer.setPhone(jTextFieldPhone.getText());
                this.customer.setEmail(jTextFieldEmail.getText());
                this.customer.setAddress(jTextFieldAddress.getText());
                customerDAOImpl.addCustomer(customer);
                this.dispose();
            }
        } else if (this.title.equals("Update")) {
            int response = JOptionPane.showConfirmDialog(jFrameHRMS, "Are you sure to update " + jTextFieldName.getText() + " ?", "Save confirmation", JOptionPane.YES_NO_OPTION);
            if (response == JOptionPane.YES_OPTION) {
                this.customer.setName(jTextFieldName.getText());
                this.customer.setPhone(jTextFieldPhone.getText());
                this.customer.setEmail(jTextFieldEmail.getText());
                this.customer.setAddress(jTextFieldAddress.getText());
                customerDAOImpl.updateCustomer(customer);
                this.dispose();
            }
        } else if (this.title.equals("Delete")) {
            int response = JOptionPane.showConfirmDialog(jFrameHRMS, "Are you sure to delete " + jTextFieldName.getText() + " ?", "Save confirmation", JOptionPane.YES_NO_OPTION);
            if (response == JOptionPane.YES_OPTION) {
                customerDAOImpl.deleteCustomer(customer);
                this.dispose();
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed


